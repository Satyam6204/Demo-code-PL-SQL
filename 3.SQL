DECLARE
    V_EMPNO EMP.EMPNO%TYPE;
	V_ENAME EMP.ENAME%TYPE;
	V_SAL EMP.SAL%TYPE;
BEGIN
     V_EMPNO := &EMPNO;
	 
	 SELECT ENAME, SAL INTO V_ENAME,V_SAL FROM EMP
	 WHERE EMPNO = V_EMPNO;
	 
	 DBMS_OUTPUT.PUT_LINE(V_ENAME||'   '||V_SAL);
	 
	 DBMS_OUTPUT.PUT_LINE('THIS IS SELECT QUERY WITH %TYPE = THIS IS USE FOR ACCESSING DATA TYPE   ');
END;
