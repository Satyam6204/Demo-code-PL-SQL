DECLARE
   V_EMPNO EMP.EMPNO%TYPE;
   R EMP%ROWTYPE;
   
BEGIN
   V_EMPNO := &EMPNO;
   
   SELECT * INTO R FROM EMP
   WHERE EMPNO = V_EMPNO;
   
   DBMS_OUTPUT.PUT_LINE(R.ENAME||'  ' ||R.SAL);
   
   DBMS_OUTPUT.PUT_LINE('THIS IS SELECT QUERY WITH %ROWTYPE ROWTYPE ARE HOLDING ALL COLUMNS');
END;
