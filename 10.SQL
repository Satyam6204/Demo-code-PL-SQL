DECLARE
     V_EMPNO EMP.EMPNO%TYPE;
	 V_JOB EMP.JOB%TYPE;
	 V_PER FLOAT;
BEGIN
     V_EMPNO := &EMPNO;
	 
	 SELECT JOB INTO V_JOB FROM EMP
	 WHERE EMPNO=V_EMPNO;
	 
	 IF V_JOB='MANAGER'THEN
	    V_PER :=30;
	 
	 ELSIF V_JOB='CLERK' THEN
	     V_PER := 20;
	ELSE 
	   V_PER :=10;
	   END IF;
	   
	 UPDATE EMP SET SAL= SAL+SAL*V_PER/100
	 WHERE EMPNO= V_EMPNO;
	 
	 COMMIT;
	 
	 DBMS_OUTPUT.PUT_LINE('JOB =' || V_JOB);
	 DBMS_OUTPUT.PUT_LINE(V_PER || '% INCREASED SALARY');
	 
END;
